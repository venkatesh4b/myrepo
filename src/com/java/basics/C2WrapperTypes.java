package com.java.basics;

public class C2WrapperTypes {

	public static void main(String[] args) {
		
		Byte b=100;
		Short s=32000;
		Integer i=100;
		Long x = 1000000L;
		Float f = 1.1111f;
		Double d = 1.11111111111111111111;
		Character c = 'd';
		Boolean bl = true;

		String str = "selenium";
		System.out.println(str);
		
		Object obj = 10;
		
		System.out.println(obj);
		
		obj = "hi";
		System.out.println(obj);
		
		obj=1.11;
		System.out.println(obj);
		
		//int, double, boolean, String, Object
	}

}







